package Recherche;

public class Personne {

    private int Num;

    private String Nom;

    private String Prenom;

    private int Tel;

    private String Adresse;

    public Personne(int Num, String Nom, String Prenom, int Tel, String Adresse) {
        this.Num = Num;
        this.Nom = Nom;
        this.Prenom = Prenom;
        this.Tel = Tel;
        this.Adresse = Adresse;
    }
    
    
}
